#include <libft.h>

int			ft_strcequ(char *s1, char *s2, char c)
{
	while (*s1 == *s2 && *s1 && *s2)
	{
		if (*s1 == c)
			return (1);
		s1++;
		s2++;
	}
	if (*s1 == 0 && *s2 == 0)
		return (1);
	return (0);
}
